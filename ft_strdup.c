#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	char	*cpy;
	int		i;

	i = 0;
	len = ft_strlen(s);
	cpy = (char *)malloc(sizeof(char) * (len + 1));
	if (!cpy)
		return (NULL);
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
